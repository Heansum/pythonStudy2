'''
계산기 모듈
'''

def add(num1, num2):
    print("덧셈이 실행됩니다.")
    result = num1 + num2

    print("연산의 결과는 {} 입니다.".format(result))
    print("결괏값을 반환합니다.")

    return result

def minus(num1, num2):
    print("뺄셈이 실행됩니다.")
    result = num1 - num2

    print("연산의 결과는 {} 입니다.".format(result))
    print("결괏값을 반환합니다.")

    return result

